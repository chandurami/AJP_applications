package Commerce;

public class Commercemodel {
	String name;
	String id;
	int size;
	int cost;
	public String getName() {
	return name;
	}
	public void setName(String name) {
	this.name = name;
	}
	public String getId() {
	return id;
	}
	public void setId(String id) {
	this.id = id;
	}
	public int getSize() {
	return size;
	}
	public void setSize(int size) {
	this.size = size;
	}
	public int getCost() {
	return cost;
	}
	public void setCost(int cost) {
	this.cost = cost;
	}
}
